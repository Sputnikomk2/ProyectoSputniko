class Aprendizaje:
    def __init__(self, nivel1):
        self.nivel = nivel1

    def obtener_nivel1(self):
        return self.nivel1

    
    def __innit__(self,nivel2):
        self.nivel2=nivel2

    def obtener_nivel2(self):
        return self.nivel2

# Crear instancias de la clase Aprendizaje
aprendizaje_1 = Aprendizaje(nivel=1)
aprendizaje_2 = Aprendizaje(nivel=2)



